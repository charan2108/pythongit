# We can assign multiple variables to multiple values in a single
# during assignment the variable must be on left hand side and  must be equal to the value on the right hand side
# If they are not equal then results in errors.

# Example
a,b,c = 10,20,30
print(a,b,c)
# Example 2
a,b,c = 10,20
print(a,b,c)
# Example3
b,c = 10,20,30
print(b,c)

# Assigning single value to multiple variables
# Example4
a=b=c = 10
print(a,b,c)
